import UIKit

extension UIButton{
    func setSelectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.9511083961, green: 0.5080775619, blue: 0.5080775619, alpha: 1)
        
    }
    func setDeslectedColor(){
        self.backgroundColor = #colorLiteral(red: 1, green: 0.7502699742, blue: 0.6630077499, alpha: 1)
    }
}
